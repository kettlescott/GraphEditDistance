package com.automation.graph;

public class GraphJsonString {

    private String ui_json ;

    private String graph_json ;


    void setUi_json (String ui_json){
        this.ui_json = ui_json ;
    }

    void setGraph_json (String graph_json){
        this.graph_json = graph_json ;
    }

    public String getUi_json () {
        return ui_json;
    }

    public String getGraph_json(){
        return graph_json ;
    }
}
